    _flatItems: null,

    _buildFlatItems: function() {
        if (this._flatItems) return;
        this._flatItems = [];
        for (var i = 0; i < this._categories.length; i++) {
            var cat = this._categories[i];
            var path = (cat.parent ? cat.parent + "/" : "") + cat.name;
            for (var j = 0; j < cat.items.length; j++) {
                this._flatItems.push({
                    term: cat.items[j],
                    path: path,
                    id: "pop_" + i + "_" + j
                });
            }
        }
    },

    search: async function (query, page, type) {
        page = page || 1;
        query = query || "";
        
        this._buildFlatItems();
        var flat = this._flatItems;

        // 菜单触发词
        if (query === "分类" || query === "菜单" || query === "帮助" || query === "艺人") {
            var cats = this._categories;
            var items = [];
            for (var i = 0; i < cats.length; i++) {
                items.push({
                    id: "cat_" + i,
                    title: cats[i].name,
                    artist: "",
                    album: cats[i].name,
                    duration: 0,
                    artwork: "",
                    url: ""  // 添加url字段
                });
            }
            var pageSize = 10;
            var start = (page - 1) * pageSize;
            var end = start + pageSize;
            var pageItems = items.slice(start, end);
            return { isEnd: end >= items.length, data: pageItems };
        }

        // 精确匹配艺人名 → 展开歌曲
        var q = query.toLowerCase();
        for (var i = 0; i < this._categories.length; i++) {
            var cat = this._categories[i];
            if (cat.name.toLowerCase() === q) {
                var songs = cat.items;
                var pageSize = 10;
                var start = (page - 1) * pageSize;
                var end = start + pageSize;
                var pageSongs = songs.slice(start, end);
                var items = [];
                for (var j = 0; j < pageSongs.length; j++) {
                    items.push({
                        id: "song_" + i + "_" + (start + j),
                        title: pageSongs[j],
                        artist: cat.name,
                        album: pageSongs[j],
                        duration: 0,
                        artwork: "",
                        url: ""  // 添加url字段
                    });
                }
                return { isEnd: end >= songs.length, data: items };
            }
        }

        // 模糊匹配
        var matched = [];
        for (var i = 0; i < flat.length; i++) {
            var item = flat[i];
            if (item.term.toLowerCase().indexOf(q) !== -1 || item.path.toLowerCase().indexOf(q) !== -1) {
                matched.push(item);
            }
        }

        var pageSize = 10;
        var start = (page - 1) * pageSize;
        var end = start + pageSize;
        var pageItems = matched.slice(start, end);
        var isEnd = end >= matched.length;

        var items = [];
        for (var j = 0; j < pageItems.length; j++) {
            var m = pageItems[j];
            items.push({
                id: m.id + "_" + (page * 1000 + j),
                title: m.term,
                artist: m.path,
                album: m.term,
                duration: 0,
                artwork: "",
                url: ""  // 添加url字段
            });
        }

        return { isEnd: isEnd, data: items };
    },

