    // 预构建扁平搜索数组
    _flatItems: null,

    _buildFlatItems: function() {
        if (this._flatItems) return;
        this._flatItems = [];
        for (var i = 0; i < this._categories.length; i++) {
            var cat = this._categories[i];
            var path = (cat.parent ? cat.parent + "/" : "") + cat.name;
            for (var j = 0; j < cat.items.length; j++) {
                this._flatItems.push({
                    term: cat.items[j],
                    path: path,
                    id: "item_" + i + "_" + j
                });
            }
        }
    },

    // 搜索主函数
    search: async function (query, page, type) {
        page = page || 1;
        // 构建索引（首次调用）
        this._buildFlatItems();
        var flat = this._flatItems;

        // 处理特殊命令
        if (query === "分类" || query === "菜单" || query === "帮助") {
            return this._getCategoryMenu();
        }

        // 搜索匹配的关键词（不区分大小写）
        var q = query.toLowerCase();
        var matched = [];
        for (var i = 0; i < flat.length; i++) {
            var item = flat[i];
            if (item.term.toLowerCase().indexOf(q) !== -1 || item.path.toLowerCase().indexOf(q) !== -1) {
                matched.push(item);
            }
        }

        // 分页处理
        var pageSize = 10;
        var start = (page - 1) * pageSize;
        var end = start + pageSize;
        var pageItems = matched.slice(start, end);
        var isEnd = end >= matched.length;

        // 转换为MusicFree条目
        var items = [];
        for (var j = 0; j < pageItems.length; j++) {
            var m = pageItems[j];
            var title = this._truncate(m.term, 10);
            var album = this._truncate(m.path, 10);
            items.push({
                id: m.id + "_" + (page * 1000 + j),
                title: title,
                artist: "",
                album: album,
                duration: 0,
                artwork: ""
            });
        }

        return {
            isEnd: isEnd,
            data: items
        };
    },

    _getCategoryMenu: function() {
        var cats = this._categories;
        var items = [];
        for (var i = 0; i < cats.length; i++) {
            var c = cats[i];
            items.push({
                id: "cat_" + i,
                title: this._truncate(c.name, 10),
                artist: "",
                album: this._truncate(c.parent || "主分类", 10),
                duration: 0,
                artwork: ""
            });
        }
        return { isEnd: true, data: items };
    },

    _truncate: function(str, len) {
        return str.length > len ? str.substring(0, len) : str;
    },

